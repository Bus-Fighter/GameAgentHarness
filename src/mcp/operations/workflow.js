// ─── dev_loop: session state file memory ─────────────────────────────────────

export function formatSessionState(state) {
  const lines = ["# Session State", "", "> Auto-generated by dev_loop save_state", "", "## Current Task", "", state.current_task, ""];
  if (state.epic || state.feature) {
    lines.push("## Context", "");
    if (state.epic) lines.push(`- **Epic**: ${state.epic}`);
    if (state.feature) lines.push(`- **Feature**: ${state.feature}`);
    lines.push("");
  }
  if (state.files_modified?.length) {
    lines.push("## Files Modified", "");
    for (const f of state.files_modified) lines.push(`- \`${f}\``);
    lines.push("");
  }
  if (state.decisions?.length) {
    lines.push("## Key Decisions", "");
    for (const d of state.decisions) lines.push(`- ${d}`);
    lines.push("");
  }
  if (state.open_questions?.length) {
    lines.push("## Open Questions", "");
    for (const q of state.open_questions) lines.push(`- [ ] ${q}`);
    lines.push("");
  }
  return lines.join("\n");
}

// ─── scene_snapshot ──────────────────────────────────────────────────────────

export function genSceneSnapshotScript(resPath, maxDepth) {
  return `${SCENE_TREE_HEADER}
func _initialize():
\tvar packed := load("${gdEscape(resPath)}")
\tif packed == null:
\t\t_mcp_output("error", "Failed to load scene: ${gdEscape(resPath)}")
\t\t_mcp_done()
\t\treturn
\tvar instance: Node = packed.instantiate()
\tvar _root: Node = _mcp_get_root()
\tif _root == null:
\t\t_mcp_output("error", "Scene root not available")
\t\tinstance.queue_free()
\t\t_mcp_done()
\t\treturn
\t_root.add_child(instance)
\tvar data := _snap(instance, ${maxDepth}, 0)
\t_mcp_output("snapshot", data)
\tinstance.queue_free()
\t_mcp_done()

func _snap(node: Node, max_depth: int, depth: int) -> Dictionary:
\tvar info := {"name": node.name, "type": node.get_class()}
\tif node is Node2D:
\t\tinfo["position"] = {"x": node.position.x, "y": node.position.y}
\t\tinfo["rotation"] = node.rotation
\tif node is Node3D:
\t\tinfo["position"] = {"x": node.position.x, "y": node.position.y, "z": node.position.z}
\tif depth < max_depth:
\t\tvar ch: Array = []
\t\tfor c in node.get_children():
\t\t\tch.append(_snap(c, max_depth, depth + 1))
\t\tinfo["child_count"] = ch.size()
\t\tinfo["children"] = ch
\treturn info
`;
}

export function flattenSnapshot(node, prefix = "") {
  const pathStr = prefix ? `${prefix}/${node.name}` : String(node.name);
  const rows = [{ path: pathStr, type: node.type }];
  if (Array.isArray(node.children)) {
    for (const c of node.children) rows.push(...flattenSnapshot(c, pathStr));
  }
  return rows;
}

export function diffSnapshots(before, after) {
  const beforeMap = new Map(flattenSnapshot(before).map((r) => [r.path, r.type]));
  const afterMap = new Map(flattenSnapshot(after).map((r) => [r.path, r.type]));
  const added = [];
  const removed = [];
  const type_changed = [];
  for (const [p, t] of afterMap) {
    if (!beforeMap.has(p)) added.push(p);
    else if (beforeMap.get(p) !== t) type_changed.push({ path: p, before: beforeMap.get(p), after: t });
  }
  for (const [p] of beforeMap) {
    if (!afterMap.has(p)) removed.push(p);
  }
  return { added, removed, type_changed, unchanged: [...afterMap.keys()].filter((p) => beforeMap.get(p) === afterMap.get(p)).length };
}

// ─── GDD validator (8-section standard) ──────────────────────────────────────

export const GDD_REQUIRED_SECTIONS = [
  "Overview", "Player Fantasy", "Detailed Rules", "Formulas",
  "Edge Cases", "Dependencies", "Tuning Knobs", "Acceptance Criteria",
];

export const GDD_SECTION_HINTS = {
  Overview: "Describe the system's purpose, scope, and how it fits into the larger game.",
  "Player Fantasy": "What experience does this system deliver to the player? How should it feel?",
  "Detailed Rules": "Enumerate every rule the system follows. Be explicit and unambiguous.",
  Formulas: "Define all math using named variables (e.g., damage = base_damage * multiplier), not raw numbers.",
  "Edge Cases": "List boundary conditions: min/max values, ties, empty states, overflow, etc.",
  Dependencies: "List other systems this one reads from or writes to. Use bullet format. Write 'None' if standalone.",
  "Tuning Knobs": "Identify every configurable parameter with its current default, range, and effect.",
  "Acceptance Criteria": "Write testable bullet items (- or *) that a QA engineer can verify. Each must be pass/fail.",
};

const MIN_SECTION_BODY_LENGTH = 20;

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function extractSectionBody(markdown, sectionName) {
  const headerRegex = new RegExp(`^##\\s+${escapeRegex(sectionName)}\\s*$`, "m");
  const match = headerRegex.exec(markdown);
  if (!match) return "";
  const start = match.index + match[0].length;
  const rest = markdown.slice(start);
  const nextHeader = rest.search(/^## /m);
  return (nextHeader === -1 ? rest : rest.slice(0, nextHeader)).trim();
}

function findHardcodedNumbers(body) {
  const regex = /(?<![a-zA-Z_])\b\d+(?:\.\d+)?\b/g;
  const results = [];
  let m;
  while ((m = regex.exec(body)) !== null) results.push(parseFloat(m[0]));
  return results;
}

function hasBulletItems(text) {
  return /^\s*[-*]\s+/m.test(text);
}

export function validateGDD(markdown) {
  const issues = [];
  const sections_found = [];
  const sections_missing = [];

  for (const section of GDD_REQUIRED_SECTIONS) {
    const headerRegex = new RegExp(`^##\\s+${escapeRegex(section)}\\s*$`, "m");
    if (headerRegex.test(markdown)) {
      sections_found.push(section);
    } else {
      sections_missing.push(section);
      issues.push({
        severity: "error",
        location: section,
        message: `Missing required section: ${section}`,
        suggestion: GDD_SECTION_HINTS[section],
      });
    }
  }

  for (const section of sections_found) {
    const body = extractSectionBody(markdown, section);
    if (body.length < MIN_SECTION_BODY_LENGTH) {
      issues.push({
        severity: "warning",
        location: section,
        message: `Section body is too short (${body.length} chars, minimum ${MIN_SECTION_BODY_LENGTH})`,
        suggestion: `Expand the ${section} section. ${GDD_SECTION_HINTS[section]}`,
      });
    }
  }

  if (sections_found.includes("Formulas")) {
    const body = extractSectionBody(markdown, "Formulas");
    if (body.length > 0) {
      const numbers = findHardcodedNumbers(body);
      if (numbers.length > 0) {
        issues.push({
          severity: "warning",
          location: "Formulas",
          message: `Hardcoded numbers found: ${numbers.join(", ")}`,
          suggestion: "Replace raw numbers with named tuning variables (e.g., base_damage, multiplier).",
        });
      }
    }
  }

  if (sections_found.includes("Acceptance Criteria")) {
    const body = extractSectionBody(markdown, "Acceptance Criteria");
    if (body.length > 0 && !hasBulletItems(body)) {
      issues.push({
        severity: "warning",
        location: "Acceptance Criteria",
        message: "Acceptance Criteria should use bullet list items (- or *) for testable checks",
        suggestion: "Rewrite as a bullet list, e.g.:\n- [ ] When player deals damage, health decreases by the expected amount",
      });
    }
  }

  if (sections_found.includes("Dependencies")) {
    const body = extractSectionBody(markdown, "Dependencies");
    if (body.length > 0 && body.toLowerCase() !== "none" && !hasBulletItems(body)) {
      issues.push({
        severity: "warning",
        location: "Dependencies",
        message: "Dependencies section should use bullet list format or state 'None'",
        suggestion: "List dependencies as bullet items, e.g.:\n- Combat system (reads player stats)\n- UI system (displays health bar)",
      });
    }
  }

  const passed = sections_missing.length === 0 && !issues.some((i) => i.severity === "error");
  return { passed, sections_found, sections_missing, issues };
}

// ─── Chain-of-Verification ───────────────────────────────────────────────────

const COV_QUESTION_TEMPLATES = [
  "What if the analysis {reason} — could the verdict '{verdict}' be based on incomplete context from '{context}'?",
  "Could there be hidden assumptions in the verdict '{verdict}' that might not hold given the context '{context}'?",
  "Might this verdict overlook edge cases or boundary conditions that '{context}' does not explicitly address?",
  "Is it possible that '{context}' contains misleading signals that led to the verdict '{verdict}'?",
  "What if the evidence in '{context}' is wrong or outdated — would the verdict '{verdict}' still be valid?",
];

const WEAK_SIGNALS_CONTEXT = ["skipped", "only checked", "partial"];
const WEAK_SIGNALS_VERDICT = ["concerns", "fail"];

export function chainOfVerification(verdict, context) {
  const reason = "missed critical interactions between subsystems";
  const questions = COV_QUESTION_TEMPLATES.map((tpl) =>
    tpl.replace(/\{verdict\}/g, verdict).replace(/\{context\}/g, context).replace(/\{reason\}/g, reason));

  let confidence = 0.9;
  const contextLower = context.toLowerCase();
  const verdictLower = verdict.toLowerCase();
  for (const signal of WEAK_SIGNALS_CONTEXT) if (contextLower.includes(signal)) confidence -= 0.15;
  for (const signal of WEAK_SIGNALS_VERDICT) if (verdictLower.includes(signal)) confidence -= 0.15;
  if (context.length < 30) confidence -= 0.15;
  confidence = Math.max(0.1, Math.min(1.0, confidence));

  let recommendation;
  if (confidence < 0.7) recommendation = "Low confidence — recommend manual review before proceeding";
  else if (confidence < 0.9) recommendation = "Moderate confidence — verify key concerns manually";
  else recommendation = "High confidence — verdict is well-supported by evidence";

  return { original_verdict: verdict, questions, confidence, recommendation };
}

