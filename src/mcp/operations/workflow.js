// ─── Handler ─────────────────────────────────────────────────────────────────

export async function handle(toolName, args, ctx) {
  try {
    switch (toolName) {
      case "dev_loop": {
        const projectPath = requireProjectPath(args);
        const code = args.code;
        if (typeof code !== "string" || code.trim() === "") {
          return opsErrorResult("INVALID_PARAMS", '"code" must be a non-empty string.');
        }
        const verify = args.verify === true;
        const timeout = Math.min(Math.max(Math.round(Number(args.timeout) || 30), 5), 120);
        const godot = await resolveGodotPath(args.godot_path ?? ctx.godotPath);

        const execResult = await executeGdscript({ godotPath: godot, projectPath, code, timeout });
        const result = { step1_execute: execResult.compile_success ? "success" : "compile_error" };

        if (!execResult.compile_success) {
          result.compile_error = execResult.compile_error;
          return textResult(JSON.stringify(result, null, 2));
        }
        if (!execResult.run_success) {
          result.step1_execute = "runtime_error";
          result.run_error = execResult.run_error;
          return textResult(JSON.stringify(result, null, 2));
        }

        const outputs = {};
        for (const entry of execResult.outputs) {
          try { outputs[entry.key] = JSON.parse(entry.value); } catch { outputs[entry.key] = entry.value; }
        }
        result.outputs = outputs;

        if (verify) {
          const v = await spawnGodot(godot, ["--headless", "--path", projectPath, "-e", "--quit"], { timeoutMs: 20000 });
          if (v.timedOut) {
            result.step2_verify = { status: "timeout", output: v.stdout.slice(-2000) };
          } else {
            const combined = `${v.stdout}\n${v.stderr}`;
            const errorLines = combined.split("\n").filter((l) => l.includes("SCRIPT ERROR") || l.includes("Parse Error"));
            result.step2_verify = errorLines.length > 0
              ? { status: "error", errors: errorLines.slice(0, 10) }
              : { status: "ok" };
          }
        }

        if (args.save_state && typeof args.save_state === "object" && !Array.isArray(args.save_state)) {
          const ss = args.save_state;
          let fileName = typeof ss.file_name === "string" && ss.file_name ? ss.file_name : "active.md";
          if (!/^[\w-]+\.md$/.test(fileName)) {
            return opsErrorResult("INVALID_PARAMS", 'save_state.file_name must match *.md with no directories (letters/digits/_/-)');
          }
          const stateDir = path.join(ctx.traceDir, "dev-loop-state");
          fs.mkdirSync(stateDir, { recursive: true });
          const fullPath = path.join(stateDir, fileName);
          const content = formatSessionState({
            current_task: ss.task || "Unknown task",
            epic: ss.epic,
            feature: ss.feature,
            files_modified: ss.files_modified,
            decisions: ss.decisions,
            open_questions: ss.open_questions,
          });
          writeAtomic(fullPath, content);
          result.save_state = { saved: true, path: fullPath };
        }

        return textResult(JSON.stringify(result, null, 2));
      }

      case "scene_snapshot": {
        const projectPath = requireProjectPath(args);
        const scenePath = args.scene_path;
        if (!scenePath) return opsErrorResult("INVALID_PARAMS", '"scene_path" is required for scene_snapshot.');
        const rel = normalizeUserProjectPath(String(scenePath));
        try { resolveWithinRoot(projectPath, rel); } catch {
          return opsErrorResult("INVALID_PATH", "scene_path contains path traversal");
        }
        const maxDepth = Math.min(Math.max(Math.floor(Number(args.max_depth) || 5), 1), 20);
        const resPath = rel.startsWith("res://") ? rel : `res://${rel.replace(/\\/g, "/")}`;
        const godot = await resolveGodotPath(args.godot_path ?? ctx.godotPath);
        const execResult = await executeGdscript({ godotPath: godot, projectPath, code: genSceneSnapshotScript(resPath, maxDepth), timeout: 30, trusted: true });
        const parsed = parseGdscriptResult(execResult, { mapError: () => "SNAPSHOT_FAILED" });
        if (parsed.isError) return parsed;
        const payload = JSON.parse(parsed.content[0].text);
        let snapshot = payload.snapshot;
        if (typeof snapshot === "string") {
          try { snapshot = JSON.parse(snapshot); } catch { /* keep string */ }
        }
        const out = { success: true, snapshot };
        if (args.before && typeof args.before === "object" && snapshot && typeof snapshot === "object") {
          out.comparison = diffSnapshots(args.before, snapshot);
        }
        return textResult(JSON.stringify(out, null, 2));
      }

      case "batch_validate": {
        const projectPath = requireProjectPath(args);
        const scripts = args.scripts;
        if (!Array.isArray(scripts) || scripts.length === 0) {
          return opsErrorResult("INVALID_PARAMS", '"scripts" must be a non-empty array of file paths.');
        }
        if (scripts.length > 50) {
          return opsErrorResult("INVALID_PARAMS", `Too many scripts (${scripts.length}). Maximum: 50`);
        }
        const godot = await resolveGodotPath(args.godot_path ?? ctx.godotPath);
        const perFileTimeout = Math.min(Math.max(Math.round(Number(args.timeout) || 15), 5), 60);

        const results = {};
        let allValid = true;
        for (const s of scripts) {
          const rel = normalizeUserProjectPath(String(s));
          let abs;
          try { abs = resolveWithinRoot(projectPath, rel); } catch {
            results[s] = { valid: false, error: "Path traversal" };
            allValid = false;
            continue;
          }
          if (!fs.existsSync(abs)) {
            results[s] = { valid: false, error: "File not found" };
            allValid = false;
            continue;
          }
          const res = await spawnGodot(godot, ["--headless", "--path", projectPath, "--check-only", "--script", abs], { timeoutMs: perFileTimeout * 1000 });
          const combined = `${res.stdout}\n${res.stderr}`;
          const errorLines = combined.split("\n").filter((l) => l.includes("SCRIPT ERROR") || l.includes("Parse Error"));
          if (res.timedOut) {
            results[s] = { valid: false, error: "Validation timed out" };
            allValid = false;
          } else if (errorLines.length > 0) {
            results[s] = { valid: false, error: errorLines.map((l) => l.trim()).slice(0, 5) };
            allValid = false;
          } else {
            results[s] = { valid: true };
          }
        }
        return textResult(JSON.stringify({ all_valid: allValid, total: scripts.length, results }, null, 2));
      }

      case "validate_gdd": {
        const projectPath = requireProjectPath(args);
        const gddPath = args.gdd_path;
        if (!gddPath || typeof gddPath !== "string") {
          return opsErrorResult("INVALID_PARAMS", "gdd_path is required");
        }
        let fullPath;
        try { fullPath = resolveWithinRoot(projectPath, gddPath); } catch {
          return opsErrorResult("INVALID_PATH", "gdd_path contains path traversal");
        }
        if (!fs.existsSync(fullPath)) {
          return opsErrorResult("FILE_NOT_FOUND", `GDD file not found: ${gddPath}`);
        }
        const content = fs.readFileSync(fullPath, "utf8");
        return textResult(JSON.stringify(validateGDD(content), null, 2));
      }

      case "chain_verify": {
        const verdict = String(args.verdict ?? "");
        const context = String(args.context ?? "");
        if (!verdict) return opsErrorResult("INVALID_PARAMS", "verdict is required");
        if (!context) return opsErrorResult("INVALID_PARAMS", "context is required");
        return textResult(JSON.stringify(chainOfVerification(verdict, context), null, 2));
      }

      case "list_templates": {
        const tag = args.tag ? String(args.tag).toLowerCase() : undefined;
        let filtered = TEMPLATES;
        if (tag) {
          filtered = filtered.filter((t) => t.name.toLowerCase().includes(tag) || t.description.toLowerCase().includes(tag));
        }
        const lines = filtered.map((t) => `- **${t.id}**: ${t.name} — ${t.description} (params: ${t.params.map((p) => p.name).join(", ") || "none"})`);
        return textResult(`Available templates (${filtered.length}, core set of 4 ported):\n${lines.join("\n")}`);
      }

      case "apply_template": {
        const projectPath = requireProjectPath(args);
        const templateId = String(args.template_id ?? "");
        const scriptPathRaw = String(args.script_path ?? "");
        if (!templateId) return opsErrorResult("INVALID_PARAMS", "template_id is required");
        if (!scriptPathRaw) return opsErrorResult("INVALID_PARAMS", "script_path is required");
        const template = TEMPLATES.find((t) => t.id === templateId);
        if (!template) {
          return opsErrorResult("TEMPLATE_NOT_FOUND", `Template '${templateId}' not found. Available: ${TEMPLATES.map((t) => t.id).join(", ")}`);
        }
        const rel = normalizeUserProjectPath(scriptPathRaw);
        let fullPath;
        try { fullPath = resolveWithinRoot(projectPath, rel); } catch {
          return opsErrorResult("INVALID_PATH", "script_path contains path traversal");
        }
        const userVars = args.variables && typeof args.variables === "object" ? args.variables : {};
        const variables = {};
        try {
          for (const param of template.params) {
            const raw = String(userVars[param.name] ?? param.default);
            variables[param.name] = sanitizeTemplateValue(raw);
          }
        } catch (err) {
          return opsErrorResult("INVALID_PARAMS", err.message);
        }
        const code = template.generate(variables);
        ensureDir(fullPath);
        writeAtomic(fullPath, code);
        return textResult(`Template '${template.name}' applied to ${rel} (${code.split("\n").length} lines)`);
      }

      default:
        return opsErrorResult("UNKNOWN_TOOL", `No handler for tool: ${toolName}`);
    }
  } catch (err) {
    return opsErrorResult("INVALID_PARAMS", err.message);
  }
}
